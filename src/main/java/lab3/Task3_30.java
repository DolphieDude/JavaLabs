package lab3;

import java.util.Arrays;
import java.util.HashMap;

public class Task3_30 {
    public static void main(String[] args) {
        System.out.println("There are " + args.length + " arguments");
        System.out.println("The arguments:" +  Arrays.toString(args));

        HashMap<Character, Integer> characterFrequency = new HashMap<>();
        for (String arg: args) {
            for (char character: arg.toCharArray()) {
                characterFrequency.put(character,
                        characterFrequency.containsKey(character) ? characterFrequency.get(character) + 1 : 1);
            }
        }

        characterFrequency.forEach((key, value) -> System.out.println(key + ": " + value + " times"));
    }

}
